// Export functions for filtering and searching
export const getPopularFish = () => fishProducts.filter(fish => fish.isPopular);
export const getPremiumFish = () => fishProducts.filter(fish => fish.isPremium);
export const getFishByType = (type: string) => fishProducts.filter(fish => fish.type === type);
export const getFishByTag = (tag: string) => fishProducts.filter(fish => fish.tags.includes(tag));
export const searchFish = (query: string) => {
  const searchTerm = query.toLowerCase();
  return fishProducts.filter(fish => 
    fish.tanglishName.toLowerCase().includes(searchTerm) ||
    fish.englishName.toLowerCase().includes(searchTerm) ||
    fish.tags.some(tag => tag.toLowerCase().includes(searchTerm))
  );
};

